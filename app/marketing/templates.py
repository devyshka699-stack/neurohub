"""Шаблоны откликов. Меняются в зависимости от типа запроса и площадки."""

import random

from .. import config

# Заголовок услуги и ориентировочная цена/срок для каждого типа задачи
_OFFERS = {
    "text": ("напишу качественный текст", "от 500₽", "30 минут"),
    "image": ("сгенерирую уникальную картинку по описанию", "от 300₽", "15 минут"),
    "background": ("аккуратно уберу фон с фото", "от 200₽", "10 минут"),
    "tts": ("озвучу текст живым голосом", "от 400₽", "20 минут"),
    "colorize": ("раскрашу чёрно-белое фото", "от 350₽", "20 минут"),
    "upscale": ("увеличу разрешение фото без потери качества", "от 250₽", "15 минут"),
    "logo": ("создам логотип в нескольких вариантах", "от 600₽", "40 минут"),
}

# Несколько вариаций вступления, чтобы отклики не были идентичными
_OPENERS = [
    "Здравствуйте! Готов помочь с вашей задачей.",
    "Добрый день! Возьмусь за ваш заказ.",
    "Привет! Могу выполнить это для вас.",
    "Здравствуйте! Как раз специализируюсь на таких задачах.",
]

_CLOSERS = [
    "Работаю на нейросетях, поэтому быстро и недорого.",
    "Использую современные AI-инструменты — качество на уровне.",
    "Результат пришлю в оговорённые сроки.",
    "Готов приступить прямо сейчас.",
]


def build_reply(task: str | None, source: str) -> str:
    action, price, eta = _OFFERS.get(
        task or "text", ("выполню вашу задачу", "договорная", "30 минут")
    )
    opener = random.choice(_OPENERS)
    closer = random.choice(_CLOSERS)
    portfolio = config.PORTFOLIO_URL

    return (
        f"{opener} {action[0].upper() + action[1:]}.\n"
        f"Сделаю за {eta}. Цена: {price}.\n"
        f"{closer}\n"
        f"Портфолио и заказ: {portfolio}"
    )
